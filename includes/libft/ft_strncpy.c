#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	char	*temp_s1;

	temp_s1 = s1;
	while (n && *s2 != '\0')
	{
		*s1++ = *s2++;
		n--;
	}
	while (n--)
		*s1++ = '\0';
	return (temp_s1);
}
